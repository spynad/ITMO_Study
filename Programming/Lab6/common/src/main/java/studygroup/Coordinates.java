package studygroup;

/**
 * x-y координаты.
 */
public class Coordinates {
    private double x;
    private double y;

    public Coordinates(double x, double y) {
        this.x = x;
        this.y = y;
    }

    /**
     * @return x координата.
     */
    public double getX() {
        return x;
    }

    /**
     * @return y координата.
     */
    public double getY() {
        return y;
    }
}
